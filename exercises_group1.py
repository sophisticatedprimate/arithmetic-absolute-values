# |4|
s1 = abs(4)
print(f'|4| = {s1}')

# |0|
s2 = abs(0)
print(f'|0| = {s2}')

# |-9|
s3 = abs(-9)
print(f'|-9| = {s3}')

# |-14|
s4 = abs(-14)
print(f'|-14| = {s4}')

# -|4|
s5 = -abs(4)
print(f'-|4| = {s5}')

# -|-8|
s6 = -abs(-8)
print(f'-|-8| = {s6}')

# -|3|
s7 = -abs(3)
print(f'-|3| = {s7}')

# |7-8|
s8 = abs(7-8)
print(f'|7-8| = {s8}')

# |3-10|
s9 = abs(3-10)
print(f'|3-10| = {s9}')

# |2||3|
s10 = abs(2)*abs(3)
print(f'|2||10| = {s10}')

# |-4||-3|
s11 = abs(-4)*abs(-3)
print(f'|-4||-3| = {s11}')

# |-(6/2)|
s12 = abs(-(6/2))
print(f'|-(6/2)| = {s12}')

# |-9| / |-3|
s13 = abs(-9)/abs(-3)
print(f'|-9| / |-3| = {s13}')

# -90 / |10|
s14 = -90 / abs(10)
print(f'-90 / |10| = {s14}')
